package com.JavaPractise;

public class P2StringConcat {
	public static void main(String[] args) {
		
		
		//+ concat operator
		//print ln to print the new line in console after the print statement
		int a=10;
		int b=30;
		
		double d=10.09;
		double e=89.91;
		
		String x="Nayeem";
		String y="John";
		
		System.out.println(a+b);
		System.out.println(x+y);
		System.out.println(a+b+x+y);
		System.out.println(x+y+a+b);
		System.out.println(x+y+(a+b));
		System.out.println(x+(a+b)+y+x+a+(a+b)+a+b);
		
		System.out.println("x value ="+x);
		System.out.println("a+b value ="+(a+b));
		System.out.println(d+e);
		System.out.println(x+y);
		System.out.println(d+e+x+y);
		System.out.println(x+y+d+e);
		System.out.println(x+y+(d+e));
		System.out.println(x+(d+e)+y+x+a+(d+e)+d+e);
		System.out.print("Hello Selenium");
		System.out.println("Hello Testing");
	}
	
	
	

}
